#include "SMG.h"

u8 smg_code[] = {0x3f,0x06,0x5b,0x4f,0x66,0x6d,0x7D,0x07,0x7f,0x6f};
u8 smg_buf[8] = {0x3f,0x3f,0x3f,0x3f,0x3f,0x3f,0x3f,0x3f};
u8 set_buf[8];
static u8 i;

void SMG_Display(u8 Display){
	DeviceCtrl(0xc0,0);
	if(Display == 0){
	DeviceCtrl(0xe0,~smg_buf[i]);
	}else{
	DeviceCtrl(0xe0,~set_buf[i]);
	}
	
	DeviceCtrl(0xc0,0x01<<i);
	i++;
	if(i == 8){
		i = 0;
	}
}